import myDialog from "@/components/dialog";
export default {
  components: {
    swiper,
    swiperSlide,
    myHead,
    myFoot,
    myDialog
  },
  data() {
    return {
      timer: null, //序列帧计时器
      count: 12,
      imgs: [],
      aniShow: true,
      aniImg: "./static/img/logo_012.jpg",
      dialogShow: false,
      icon: {
        backgroundImage:
          "url(" + require("../assets/img/index/index_icon.png") + ")"
      },
      firstName: "",
      lastName: "",
      email: "",
      tel: "",
      message: "",
      //翻页配置
      swiperOption: {
        speed: 1000,
        direction: "vertical",
        mousewheel: true,
        pagination: {
          el: ".swiper-pagination",
          clickable: true
        }
      },
      //banner配置
      bannerOption: {
        // autoplay: true,
        speed: 1000,
        // mousewheel: true,
        loop: true,
        pagination: {
          el: ".swiper-pagination-2",
          clickable: true
        },
        curPage: 0
      }
    };
  },
  //监听属性 类似于data概念
  computed: {
    swiper() {
      return this.$refs.mySwiper.swiper;
    },
    bannerSwiper() {
      return this.$refs.banner.swiper;
    }
  },
  //监控data中的数据变化
  watch: {},
  //方法集合
  methods: {
    //预加载序列帧图片
    preload() { 
      for (var i = 13; i < 118; i++) {
        if (i < 100) {
          this.imgs.push("./static/img/logo_0" + i + ".jpg");
        } else {
          this.imgs.push("./static/img/logo_" + i + ".jpg");
        }
      }
      for (let img of this.imgs) {
        let image = new Image();
        image.src = img;
        image.onload = () => {
          this.count++;
          if (this.count == 117) {
            this.ani();
          }
        };
      }
    },
    //执行序列帧动画
    ani() {
      let _count = 0;
      this.timer = setInterval(() => {
        this.aniImg = this.imgs[_count];
        _count++;
        if (_count == 105) {
          clearInterval(this.timer);
          this.aniShow = false;
        }
      }, 50);
      sessionStorage.setItem("firstLog", true);
    },
    submit() {
      this.dialogShow = true;
    },
    toService(num) {
      this.$router.push({ name: "service", params: { serviceId: num } });
    }
  },
  //生命周期 - 创建完成（可以访问当前this实例）
  created() {},
  //生命周期 - 挂载完成（可以访问DOM元素）
  mounted() {
    let isFirst = sessionStorage.getItem("firstLog");
    if (isFirst) {
      this.aniShow = false;
      return;
    } else {
      this.preload();
    }
  }
};
</script>
